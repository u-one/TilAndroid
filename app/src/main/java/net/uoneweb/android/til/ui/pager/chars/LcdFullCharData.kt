    private fun (String).toLcdBitmap(): LcdBitmap {
        return LcdBitmap(
            this.toIntArray(),
            IntSize(11, 15),
        )
    }

    private fun (String).toIntArray(): IntArray {
        return this.trim().split("\n")
            .flatMap { line ->
                line.trim().map {
                    if (it == '1') 1 else 0
                }
            }.toIntArray()
    }
